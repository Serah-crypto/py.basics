#Arithmetic Operators - simple calculations

x = 34
y = 20

print(x+y)
print(x-y)
print(x*y)
print(x/y)
print(x%y) # modulus-returns the remainder

#Comparison operators
print(x < y)
print(x > y)
print(x <= y)
print(x >= y)
print(x == y) #Equal to
print(x != y) #Not equal to

#Assignment operators
number = 10
print(number)

#Operator precedence
number += 2
print(number)

number -= 2
print(number)

number %= 2
print(number)

#Logical operators - and, or , not
num1 = 60
num2 = 12

print(num1 == num2 and num1 > num2) #and - returns a true only when all the conditions are true
print(num1 == num2 or num1 > num2) #or - returns a true if only one of the conditions is true
print(not (num1 == num2 or num1 > num2))


